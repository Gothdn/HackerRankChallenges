def pickingNumbers(a):
    count = [0 for x in range(0, 101)]
    for num in a:
        count[num] += 1
    result = 0
    for i in range(2, 101):
        result = max([result, count[i] + count[i - 1]])
    return result

if __name__ == '__main__':
    fptr = open(os.environ['OUTPUT_PATH'], 'w')

